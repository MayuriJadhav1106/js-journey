//modulus operator
let x = 9 % 5;
console.log(x);

// exponentiation operator
let z = 2 ** 5 ;
console.log(z);

//increment operator
let a = 2;
b = a++; 

let p = 5;
q = ++p; 

console.log(a);
console.log(p);
console.log(b);
console.log(q);

//decrement operator
let g = 2;
h = g++; 

let s = 5;
t = ++s; 

console.log(g);
console.log(s);
console.log(h);
console.log(t);